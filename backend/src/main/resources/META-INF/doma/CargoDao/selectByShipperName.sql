SELECT
    CARGO_ID,
    FLIGHT_NUMBER,
    ORIGIN_AIRPORT,
    DESTINATION_AIRPORT,
    CARGO_TYPE,
    WEIGHT,
    VOLUME,
    STATUS,
    SHIPPER_NAME,
    CONSIGNEE_NAME,
    CREATED_DATE,
    UPDATED_DATE,
    VERSION
FROM CARGO
WHERE SHIPPER_NAME LIKE CONCAT('%', /* shipperName */'', '%')
ORDER BY CREATED_DATE DESC 